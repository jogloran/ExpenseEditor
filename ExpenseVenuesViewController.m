- (void)configureCell:(EntryCell *)cell atIndexPath:(NSIndexPath *)indexPath
{
    NSManagedObject *object = [self.fetchedResultsController objectAtIndexPath:indexPath];
    cell.mainLabel.text = [object valueForKeyPath: @"name"];
    cell.subtitleLabel.text = [object valueForKeyPath: @"location"];
    cell.rightLabel.text = [[object valueForKeyPath: @"entries.@count"] stringValue];
    
    [cell setNeedsLayout];
}


